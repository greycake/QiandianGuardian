    /**
     * 检测是否为贵州号码
     * @param phoneNumber 电话号码（可能包含+86、区号等）
     * @return true如果是贵州号码
     */
    fun isGuizhouNumber(phoneNumber: String?): Boolean {
        if (phoneNumber.isNullOrEmpty()) {
            return false
        }

        // 清理号码：移除+86、空格、-等
        val cleanNumber = phoneNumber.replace(Regex("[+\\s\\-()]"), "")

        // 检查固定电话区号
        if (cleanNumber.length >= 11) {
            val areaCode = cleanNumber.substring(0, 4)
            if (GUIZHOU_AREA_CODES.contains(areaCode)) {
                return true
            }
        }

        // 检查手机号段
        if (cleanNumber.length == 11 && cleanNumber.startsWith("1")) {
            val prefix = cleanNumber.substring(0, 7)
            if (GUIZHOU_MOBILE_PREFIXES.contains(prefix)) {
                return true
            }
        }

        return false
    }

    /**
     * 获取号码类型
     * @param phoneNumber 电话号码
     * @return 号码类型描述
     */
    fun getNumberType(phoneNumber: String?): String {
        if (phoneNumber.isNullOrEmpty()) {
            return "未知"
        }

        val cleanNumber = phoneNumber.replace(Regex("[+\\s\\-()]"), "")

        // 固定电话
        if (cleanNumber.length >= 11) {
            val areaCode = cleanNumber.substring(0, 4)
            val areaIndex = GUIZHOU_AREA_CODES.indexOf(areaCode)
            if (areaIndex >= 0) {
                val areaNames = listOf("贵阳", "遵义", "六盘水", "黔南", "黔东南", "铜仁", "毕节", "黔西南")
                return "贵州固定电话 (${areaNames[areaIndex]})"
            }
        }

        // 手机号
        if (cleanNumber.length == 11 && cleanNumber.startsWith("1")) {
            val prefix = cleanNumber.substring(0, 3)
            return when (prefix) {
                "134", "135", "136", "137", "138", "139", "150", "151", "152", "157", "158", "159",
                "182", "183", "184", "187", "188", "178", "198" -> "贵州移动"
                "130", "131", "132", "155", "156", "185", "186", "166" -> "贵州联通"
                "133", "153", "173", "177", "180", "181", "189", "199" -> "贵州电信"
                else -> "未知运营商"
            }
        }

        return "未知"
    }
